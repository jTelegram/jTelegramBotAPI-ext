package com.jtelegram.ext.datepicker.keyboard;

/**
 * @author Nick Robson
 */
public enum DatePickerButtonType {

    /**
     * This button takes the user to a different month
     */
    GOTO_MONTH,

    /**
     * This button selects a specific date
     */
    SELECT_DATE,

    /**
     * This button has no click action and is merely a visual label
     */
    LABEL

}
